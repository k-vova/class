    const menu = document.getElementById('menu');
    const content = document.getElementById('content');
    const modal = document.getElementById('modal');
    const modalImg = document.getElementById('modalImg');

    function renderMenu(activeId) {
      menu.innerHTML = '';
      PAGES.forEach(p => {
        const btn = document.createElement('button');
        btn.textContent = p.name;
        if (p.id === activeId) btn.classList.add('active');
        btn.onclick = () => renderPage(p.id);
        menu.appendChild(btn);
      });
    }

    function renderPage(pageId) {
      renderMenu(pageId);
      const page = document.createElement('div');
      page.className = 'page active';

      const gallery = document.createElement('div');
      gallery.className = 'gallery';

      const photos = PAGES.find(p => p.id === pageId).photos;
      photos.forEach(src => addImageToGallery(src, gallery));

      page.appendChild(gallery);
      content.innerHTML = '';
      content.appendChild(page);
    }

    function addImageToGallery(src, gallery) {
      const img = document.createElement('img');
      img.src = src;
      img.onclick = () => openModal(src);
      gallery.appendChild(img);
    }

    function openModal(src) {
      modal.style.display = 'flex';
      modalImg.src = src;
    }

    function closeModal() {
      modal.style.display = 'none';
      modalImg.src = '';
    }

